export const CHINESE_WEBNOVEL_WRITING_PRINCIPLES = `
**中文网文核心原则：**

1. **用动作展现情绪，不用标签** - 永远不要写"他很悲伤"，而要展示：眼泪、垮下的肩膀、颤抖的声音。

2. **ruthless精简** - 如果一个词不推动情节、揭示角色或营造氛围，就删掉它。

3. **相信读者** - 他们能推断。他们能读懂潜台词。不要解释一切。

4. **节奏变化** - 短句制造紧张。长句带来沉浸和反思。有目的地使用两者。

5. **让每句对话都有价值** - 它应该揭示角色、推动情节或增加紧张。最好三者兼备。

6. **避免过滤** - 能省略"她看到"、"他听到"、"她感觉"时就省略。直接展示发生的事。

7. **强动词胜过副词** - "冲"胜过"快速跑"，"低语"胜过"小声说"。

8. **每段最多一个比喻** - 比喻是调料，不是主菜。慎用。

9. **限制形容词为1-2个/名词** - "旧门"不是"古老的、风化的、历经沧桑的门"。

10. **简单语言胜过花哨** - "用"胜过"使用"。"帮"胜过"促进"。清晰胜过聪明。

**网文特有原则：**

11. **爽点节奏** - 每3-5章一个小高潮（打脸、突破、获宝），每10-15章一个大高潮。

12. **悬念断章** - 每章结尾留钩子，让读者欲罢不能。

13. **金手指设定** - 外挂要有限制，不能无限制开挂。

14. **等级体系** - 实力等级要清晰，高等级对低等级要有压制感。

15. **对话要有个性** - 不同身份、修为、背景的角色说话方式要不同。
`;

